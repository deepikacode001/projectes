import mongoose from "mongoose";

const Registerschema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});
const Register = mongoose.models.Register || mongoose.model("Register", Registerschema);
export default Register;